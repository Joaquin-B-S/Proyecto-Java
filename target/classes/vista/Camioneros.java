    public void llenarJTable(ArrayList unArrayList)
    {
        // TODO add your handling code here:
        DefaultTableModel m = (DefaultTableModel) jTable1.getModel();
        m.setRowCount(0);
        

        Iterator it = unArrayList.iterator();

        int numeroColumnas = 8;
        Object[] registro = new Object[numeroColumnas];

        while (it.hasNext())
        {
            Object unObjeto = it.next();

            Camionero p = (Camionero)unObjeto;

            registro[0] = p.getRut();
            registro[1] = p.getNombre();
            registro[2] = p.getGenero();
            registro[3] = p.getEdad();
            registro[4] = p.getEstatura();
            registro[5] = p.isJubilado();
            registro[6] = p.getRuta();
            
            m.addRow(registro);
        }

        jTable1.setModel(m);
                        
    }

